package conversao;

public class ConversaoUnidades {
    public static double PolegadasCentimetros(double pol){
        return pol*2.54;
    }

    public static double PesCentimetros(double pes){
        return pes*30.48;
    }

    public static double MilhasKm(double mil){
        return mil*1.609;
    }
}
